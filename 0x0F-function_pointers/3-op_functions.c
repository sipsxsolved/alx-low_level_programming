/**
 * op_add - function name
 * @a: first operand
 * @b: second operand
 *
 * Description: a function that adds two integers
 * Return: sum of two integers
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function name
 * @a: first operand
 * @b: second operand
 *
 * Description: function that subtracts two integers
 * Return: difference between two integers
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function name
 * @a: first operand
 * @b: second operand
 *
 * Description: a function that multiplies two integers
 * Return: product of two integers
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function name
 * @a: first operand
 * @b: decond operand
 *
 * Description: function that divides two integers
 * Return: quotient of two integers
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function name
 * @a: first operand
 * @b: second operand
 *
 * Description: a function that finds the modulus of two ints
 * Return: remainder of two integers
 */

int op_mod(int a, int b)
{
	return (a % b);
}
